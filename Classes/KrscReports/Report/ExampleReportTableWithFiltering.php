/**
 * Example report creating table with filters table via Service.
 *
 * @category KrscReports
 * @package KrscReports_Report
 * @author Krzysztof Ruszczyński <http://www.ruszczynski.eu>
 */
class ExampleReportTableWithFiltering extends \KrscReports_Report_ExampleReport implements ReportWithServiceInterface
{
    /**
     * @var integer number of rows to create
     */
    protected $_iNumberOfRows = 10;

    /**
     * Method returns description of generated by that object worksheet.
     * @return string description of report
     */
    public function getDescription()
    {
        return 'Report with one table with filters table (made with \KrscReports\Service).';
    }

    /**
     * Method returning prepared view.
     *
     * @return \KrscReports\Views\AbstractView
     */
    public function getReportView()
    {
        $aColumns = array('Description', 'Measurement no 1', 'Measurement no 2');

        $aDataForExcel = array();
        $iIterator = 1;
        for ($i = 0; $i < $this->_iNumberOfRows; $i++) {
            $aDataForExcel[] = array('Example', $iIterator++, $iIterator++);
        }

        $oReportView = new SingleTableWithFilters();
        $oReportView->addOptions(
            array(
                AbstractView::KEY_DOCUMENT_PROPERTIES => array(),
                AbstractView::KEY_USE_FROM_ARRAY => true,
                SingleTableWithFilters::KEY_FILTER_TABLE_TITLE => 'Table with filters',
                SingleTableWithFilters::KEY_FILTERS => $aColumns,
                SingleTableWithFilters::KEY_FILTERS_VALUES => array(
                    'Example', '-', '-'
                ),
            )
        );

        $oReportView->setData(
            $aDataForExcel,
            $aColumns
        );

        return $oReportView;
    }

    /**
     * Method responsible for creating service with generated report.
     * @return void
     */
    public function generate()
    {
        $oExcelService = new Service();
        $oExcelService->setSpreadsheetName('First_Spreadsheet');
        $oExcelService->setFileName('Krsc_Example_12');

        $oExcelService->setReportView(
            $this->getReportView()->setDocumentProperties()
        );
        $oExcelService->createReport();
    }
}
